void	draw_map(t_data *data)
{
	int		mapX = 24, mapY = 24;
	int		x;
	int		y;

	y = -1;
	while (++y < mapY)
	{
		x = -1;
		while (++x < mapX)
		{
			if (worldMap1[y][x] > 0)
				draw_box(&data->img_map, x * 24, y * 24, 24, WHITE);
			else
				draw_box(&data->img_map, x * 24, y * 24, 24, BLACKB);
			draw_line(&data->img_map, x * 24, 0, x * 24, IMG_HEIGHT, GRAY);
			draw_line(&data->img_map, 0, y * 24, IMG_WIDTH, y * 24, GRAY);
		}
	}
}
